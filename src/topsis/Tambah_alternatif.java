    public static void edit(String nama) {
        if (nama != null) {
            try {
                Connection k = Koneksi.Go();
                Statement s = k.createStatement();
                var getid = "SELECT * FROM alternatif WHERE NAMA = '" + nama + "' ";
                ResultSet r = s.executeQuery(getid);
                if (r.next()) {
                    idToUpdate = r.getInt("id");
                    txtname.setText(r.getString("nama"));
                    gender.setSelectedItem(r.getString("jk"));
                }
                btn_simpan.setText("SIMPAN");
                labeljudul.setText("EDIT MAHASISWA");
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
